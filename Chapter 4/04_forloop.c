#include <stdio.h>

int main(){
    int n = 5;
    // incrementing for loop
    for (int i = 1; i <= n; i++)
    {
        printf("%d\n", i);
    }
    // decrementing for loop
    for (int j=5 ; j; j--)
    {
        printf("The vlaue of j in decrementing order is %d\n", j);
    }
    
    
    return 0;
}